#include<string.h>

int main() {
	char M[] = {'P', 'A', 'E', NULL};
	char* ap;
	int a = 0;
	ap = M + strlen(M) - 1; //ap = E
	printf("%c\n", *ap--);
	*--ap = 'T';
	printf("%s\n", ap);
	printf("%c\n", *++ap);
	printf("%c\n", ap[a++]);
	printf("%c\n", ap[--a]);
}

